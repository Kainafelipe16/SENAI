namespace soma23.Model {
    public class Calcular {
        public void Soma (double n1, double n2) {
            Console.WriteLine ("Soma 2: " + (n1 + n2));
        }
        public void Soma (double n1, double n2, double n3) {
            Console.Write ("Soma 3: " + (n1 + n2 + n3));
        }
    }
}